
public class InputDTO {
	private int inum;		//입고번호
	private int pnum;		//품목번호
	private String idate;	//입고일자
	private int iqty;		//입고수량
	
	
	public int getInum() {
		return inum;
	}
	public void setInum(int inum) {
		this.inum = inum;
	}
	public int getPnum() {
		return pnum;
	}
	public void setPnum(int pnum) {
		this.pnum = pnum;
	}
	public String getIdate() {
		return idate;
	}
	public void setIdate(String idate) {
		this.idate = idate;
	}
	public int getIqty() {
		return iqty;
	}
	public void setIqty(int iqty) {
		this.iqty = iqty;
	}
	
	
	
}
